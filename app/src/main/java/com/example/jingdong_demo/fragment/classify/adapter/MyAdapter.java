public class MyAdapter extends RecyclerView.Adapter {
    private final LayoutInflater inflater;
    private List<Users> select;
    private Context context;

    public MyAdapter(List<Users> select, Context context) {
        this.select = select;
        this.context = context;
        inflater = LayoutInflater.from(context);
    }

    @Override
    public RecyclerView.ViewHolder onCreateViewHolder(ViewGroup parent, int viewType) {
        View view = inflater.inflate(R.layout.activity_item, parent, false);
        MyViewHolder myViewHolder = new MyViewHolder(view);
        return myViewHolder;
    }

    @Override
    public void onBindViewHolder(RecyclerView.ViewHolder holder, int position) {
        MyViewHolder myViewHolder = (MyViewHolder) holder;
        Users users = select.get(position);
        myViewHolder.tv_item1.setText(users.getA());
    }

    @Override
    public int getItemCount() {
        return select.size();
    }

    class MyViewHolder extends RecyclerView.ViewHolder {

        private final TextView tv_item1;

        public MyViewHolder(View itemView) {
            super(itemView);
            tv_item1 = itemView.findViewById(R.id.tv_item1);
        }
    }
}
